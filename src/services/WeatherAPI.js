import axios from "axios";
const API_KEY = import.meta.env.VITE_WEATHER_API_KEY;
const BASE_URL = "https://api.openweathermap.org/data/2.5/weather";

export const getWeatherByCity = (city, signal) => {
  return axios.get(BASE_URL, {
    params: {
      q: city.trim(),
      units: "metric",
      appid: API_KEY,
    },
    signal,
  });
};
